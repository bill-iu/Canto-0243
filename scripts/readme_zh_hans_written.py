def apply_written_rules(text: str, rules: Iterable[tuple[str, str]] = _PHRASE_RULES) -> str:
    for old, new in rules:
        text = text.replace(old, new)
    for pattern, repl in _REGEX_RULES:
        text = pattern.sub(repl, text)
    return text


def find_colloquial_markers(text: str) -> list[str]:
    found: set[str] = set()
    for pattern in _COLLOQUIAL_PATTERNS:
        for match in pattern.finditer(text):
            if match.group() == "揾" and "ONE·揾·韵" in text[max(0, match.start() - 8) : match.end() + 8]:
                continue
            found.add(match.group())
    return sorted(found)


